class ArrayStack {
	int[] arr;
	int top = -1;
	
	ArrayStack(int i) {
		this.top = top+i;
	}

	int push(int num1) {
		top++;
		System.out.println("Inserted Item : " + num1); // if문작성해서 메소드 작동하게하는게좋을것같습니다.
		return num1;
	}
	
	void pop() { // 이것도 -1 되는걸 if 문으로 작성하면좋을것같습니다 return 도똑같이 -1
		top--;
		return;
	}
	
	int peek() {
		return arr[top];
	}
}
