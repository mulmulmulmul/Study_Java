
public class OperatorEx05 {

	public static void main(String[] args) {
		
		int num = 5;
		System.out.println("num : " + num);
		
		
		//선증가 연산자
		int result = ++num;
		System.out.println("num : " + result);
		
		//후증가 연산자
		result = num++;
		System.out.println("num : " + result);
		System.out.println("num : " + num);
		
	}

}
